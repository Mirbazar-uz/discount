<script setup lang="ts">
const props = defineProps<{
  daysLeft: number
}>()

const label = computed(() => {
  if (props.daysLeft <= 0) return 'Tugagan'
  if (props.daysLeft === 1) return '1 kun qoldi'
  return `${props.daysLeft} kun qoldi`
})

const isUrgent = computed(() => props.daysLeft <= 2)
</script>

<template>
  <span
    class="inline-flex items-center gap-1 font-medium"
    :class="isUrgent ? 'text-red-400' : 'text-orange-400'"
  >
    <svg
      v-if="isUrgent"
      width="12"
      height="12"
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      stroke-width="2"
      style="animation: blink 1s ease-in-out infinite"
    >
      <path d="M10.29 3.86L1.82 18a2 2 0 001.71 3h16.94a2 2 0 001.71-3L13.71 3.86a2 2 0 00-3.42 0z"/><line x1="12" y1="9" x2="12" y2="13"/><line x1="12" y1="17" x2="12.01" y2="17"/>
    </svg>
    <span :style="isUrgent ? 'animation: blink 1s ease-in-out infinite' : ''">
      {{ label }}
    </span>
  </span>
</template>
